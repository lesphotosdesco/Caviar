const container = document.getElementById("gallery-container");
const buttons = document.querySelectorAll(".filter-btn");

function renderGallery(year) {
  container.innerHTML = "";
  const years = year === "all" ? Object.keys(data) : [year];
  years.forEach(y => {
    data[y].forEach(item => {
      const el = document.createElement("div");
      el.classList.add("gallery-item");
      el.innerHTML = `<h3>${item.title}</h3><p>${item.date}</p><a href="${item.url}" target="_blank">Voir l'album</a>`;
      container.appendChild(el);
    });
  });
}

buttons.forEach(btn => {
  btn.addEventListener("click", () => {
    buttons.forEach(b => b.classList.remove("active"));
    btn.classList.add("active");
    renderGallery(btn.dataset.year);
  });
});

renderGallery("all");
